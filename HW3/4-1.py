a, b, c = eval(input("a, b, c를 입력하세요: "))

if b**2 - 4*a*c < 0:
    print("이 방정식은 실근이 존재하지 않습니다.")
elif b**2 - 4*a*c == 0:
    print("실근은",(-b + ((b**2 - 4*a*c) ** 0.5)) / (2 * a),"입니다.")
else:
    print("실근은", (-b + ((b**2 - 4*a*c) ** 0.5)) / (2 * a),"과",
          (-b - ((b**2 - 4*a*c) ** 0.5)) / (2 * a), "입니다.")
